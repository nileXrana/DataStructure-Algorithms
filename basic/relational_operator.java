public class relational_operator {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println(a == b);     // equality operator
        System.out.println(a != b);     // not equals to
        System.out.println(a > b);      
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
