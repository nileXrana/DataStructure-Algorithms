public class bitwise {
    public static void main(String[] args) {
        int a = 2, b = 4;

        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~a);
        System.out.println(b<<1);
        System.out.println(a>>1);
        
    }
}
